alter table countries enable row level security;

drop policy if exists countries_authenticated_read on countries;
create policy countries_authenticated_read on countries
  for select to authenticated
  using (is_active);

drop policy if exists countries_admin_write on countries;
create policy countries_admin_write on countries
  for all to authenticated
  using (public.is_platform_admin())
  with check (public.is_platform_admin());

drop policy if exists profiles_self_insert on profiles;
create policy profiles_self_insert on profiles
  for insert to authenticated
  with check (
    id = auth.uid()
    and role = 'supplier'
    and user_status = 'pending'
    and importer_id is null
    and supplier_id is null
  );

insert into public.profiles (id, email, full_name, role, user_status)
select
  auth_user.id,
  coalesce(auth_user.email, ''),
  coalesce(auth_user.raw_user_meta_data->>'full_name', ''),
  case
    when auth_user.raw_user_meta_data->>'role' in ('supplier', 'reviewer', 'administrator')
      then (auth_user.raw_user_meta_data->>'role')::app_role
    else 'supplier'::app_role
  end,
  case
    when auth_user.email_confirmed_at is null then 'pending'::user_status
    else 'active'::user_status
  end
from auth.users auth_user
where not exists (
  select 1
  from public.profiles profile
  where profile.id = auth_user.id
);

update public.profiles profile
set
  email = auth_user.email,
  updated_at = now()
from auth.users auth_user
where profile.id = auth_user.id
  and auth_user.email is not null
  and profile.email <> auth_user.email;
